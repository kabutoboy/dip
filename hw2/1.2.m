I = imread("Cross.pgm");
subplot(1, 5, 1);
imshow(I);
F = fft2(I, 256, 256);
F = fftshift(F);
subplot(1, 5, 2);
imshow(mat2gray(log(1+abs(F))));
subplot(1, 5, 3);
imshow(mat2gray(angle(F)));
Y = zeros(256, 256);
Y(30, 20) = 1;
Z = fft2(Y, 256, 256);
Z = fftshift(Z);
F .*= Z;
subplot(1, 5, 4);
imshow(mat2gray(angle(F)));
J = im2bw(real(ifft2(ifftshift(F))), 0.5);
subplot(1, 5, 5);
imshow(J);
