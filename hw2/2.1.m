I = imread("Cross.pgm");
F = fft2(I, 256, 256);
F = fftshift(F);
Fshow = log(1+abs(F));
G = fspecial("gaussian", 256, 12);
G /= max(max(G));
F1 = F .* im2bw(G, 0.2);
F2 = F .* G;
F1show = log(1+abs(F1));
F2show = log(1+abs(F2));
J1 = ifft2(ifftshift(F1));
J2 = ifft2(ifftshift(F2));
subplot(2, 3, 1);
imshow(I);
subplot(2, 3, 4);
imshow(I);
subplot(2, 3, 2);
imshow(F1);
subplot(2, 3, 5);
imshow(F2);
subplot(2, 3, 3);
imshow(J1);
subplot(2, 3, 6);
imshow(J2);
